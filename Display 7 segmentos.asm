	ORG 0000h
	
	MOV R1, #00h	;FORÇANDO R1 A SER 0 (PARA GARANTIA)
	MOV R0, #00h	;FORÇANDO R0 A SER 0 (PARA GARANTIA)
	MOV P2, #00h

INICIO:	MOV P2, #00h
	SETB P2.0
	CALL CONTA
	CLR P2.0
	SETB P2.1
	SETB P2.2
	SETB P2.3
	SETB P2.4
	SETB P2.5
	SETB P2.6
	CALL CONTA
	CLR P2.3
	CLR P2.4
	CLR P2.5
	CLR P2.6
	SETB P2.1
	SETB P2.2
	CALL CONTA
	CLR P2.1
	SETB P2.3
	SETB P2.4
	SETB P2.7
	SETB P2.5
	CALL CONTA
	CLR P2.5
	SETB P2.1
	CALL CONTA
	CLR P2.4
	CLR P2.3
	SETB P2.6
	CALL CONTA
	CLR P2.2
	SETB P2.3
	SETB P2.4
	CALL CONTA
	SETB P2.5
	CALL CONTA
	CLR P2.6
	CLR P2.5
	CLR P2.4
	CLR P2.7
	SETB P2.2
	CALL CONTA
	SETB P2.4
	SETB P2.5
	SETB P2.6
	SETB P2.7
	CALL CONTA
	CLR P2.5
	CALL CONTA

	JMP INICIO
	
CONTA:	SETB TR0	;INICIA O TIMER_0
LOOP:	JNB TF0, LOOP	;ENQUANTO TF0 NÃO FOR 1, EXECUTARÁ O LOOP. QUANDO VIRAR 1, IRÁ PARA AS LINHAS ABAIXO
	INC R0
	CLR TR0		;DESLIGA O TIMER, FAZ ELE PARAR DE CONTAR
	CLR TF0		;PARA DESABILITAR O FLAG
	CJNE R0, #125d, CONTA
	MOV R0, #00h
	RET		;FAZ VOLTAR NA LINHA ABAIXO DA ONDE FOI DADO O "CALL"
	
	JMP INICIO

	END